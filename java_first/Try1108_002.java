public class Try1108_002 {
    public static void main(String[] args){
        int a=9;
        int b=8;
        int c=7;
        
        System.out.println(+a);
        System.out.println(-a);
        System.out.println(a++);// a=9 印出 ， a++ 後 ，a=10
        System.out.println(a);
        System.out.println(a--);// a=10印出 ， a-- 後 ，a=9
        System.out.println(a);
        System.out.println("______________");
        System.out.println(++b);// 遇到 符號， b= b+1印出 ，b=9  印出
        System.out.println(b);
        System.out.println(--c);// 遇到 符號， c= c-1印出 ，c=6  印出
        System.out.println(c);








    }
    
}
