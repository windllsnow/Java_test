package jav_king;

public class Ch4_020 {
    public static void main(String[] args) {
        // 敘述的給合與分行
        int x, y;
        x = 5; // 可以寫成 x=5;y=10;
        y = 10;
        System.out.println("x+y=" + (x + y));
        //

        System.out.printf("byte 的範圍 %d~%d%n", // 可以在這裡 enter 分行
                Byte.MIN_VALUE, Byte.MAX_VALUE);
        System.out.printf("short的值範圍%d~%d%n",
                Short.MAX_VALUE, Short.MIN_VALUE);

    }

}
