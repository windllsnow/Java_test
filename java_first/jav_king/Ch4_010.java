package jav_king;

public class Ch4_010 {
    public static void main(String[] args) {
        //位元計算
        byte i,j;
        i=0b00000001;
        System.out.println("十進位輸出= "+i);
        System.out.println("十進位輸出(負)="+ -i);


        j= 0B01111111;
        System.out.println("十進位輸出= "+j);
        System.out.println("十進位輸出(負)="+ -j);

    }
    
}
