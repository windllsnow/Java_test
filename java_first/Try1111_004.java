public class Try1111_004 {
    public static void main(String[] args) {
        initData(99,100);
        initData(9.9,10.1);
    }
    public static void initData(int x, int y){
        System.out.println("x:"+ x);
        System.out.println("y:"+ y);
    }
    public static void initData(double x, double y){
        System.out.println("x:"+ x);
        System.out.println("y:"+ y);
    }
}

